var myArray = ['blue', 'green', 'orange', 'red'];
var counter = 0; // setup counter
while (counter < myArray.length) { // run if counter is less than array length
  console.log(myArray[counter]); // logs 'blue', 'green', 'orange', 'red'
  counter++; // add 1 to the counter
}
console.log("___________________________________________________________________________\n");